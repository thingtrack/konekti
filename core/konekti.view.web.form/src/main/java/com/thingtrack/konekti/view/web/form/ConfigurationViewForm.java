	@AutoGenerated
	private AbsoluteLayout mainLayout;
	@AutoGenerated
	private ComboBox menuCommandResourceValField;
	@AutoGenerated
	private ComboBox typeValField;
	@AutoGenerated
	private TextField valueField;
	@AutoGenerated
	private TextField tagField;
